export const MapLeafletZone = (): ReactElement => {
  const [filledZone, setFilledZone] = useState<any>(null);
  const [init, setInit] = useState<boolean>(true);

  const polylinePositions: [number, number][] = homeToWork.map((p) => [
    p[1],
    p[0],
  ]);

  useEffect(() => {
    const line = lineString(homeToWork);
    const line2 = lineString(homeToParents);
    const line3 = lineString(workToParents);

    const simplifiedLine = simplify(line, {
      tolerance: 0.0005,
      highQuality: true,
    });
    const simplifiedLine2 = simplify(line2, {
      tolerance: 0.0005,
      highQuality: true,
    });
    const simplifiedLine3 = simplify(line3, {
      tolerance: 0.0005,
      highQuality: true,
    });

    const buffer1 = buffer(simplifiedLine, 5, {
      units: "kilometers",
    });
    const buffer2 = buffer(simplifiedLine2, 5, { units: "kilometers" });
    const buffer3 = buffer(simplifiedLine3, 5, { units: "kilometers" });

    // Crer une enveloppe convexe pour remplir l'intrieur
    const allPoints = featureCollection([buffer1, buffer2, buffer3]);
    const filled = convex(allPoints);

    setFilledZone(filled);
  }, []);

  const GestureHandlingSetter = () => {
    const map = useMap() as any;

    useEffect(() => {
      // Activer gestureHandling si disponible
      if (map.gestureHandling) {
        map.gestureHandling.enable();
      }

      setInit(false);
    }, [map]);

    return null;
  };

  return (
    <ZoneContainer center={[45.5, 4.5]} zoom={10} scrollWheelZoom={true}>
      <TileLayer
        url="https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png"
        attribution=" OpenStreetMap"
      />

      {/* Affichage de la zone remplie (enveloppe convexe) */}
      {filledZone && (
        <GeoJSON
          data={filledZone}
          style={{
            color: "blue",
            fillColor: "blue",
            fillOpacity: 0.2,
            weight: 2,
          }}
        />
      )}

      <FitBoundsToPolyline positions={polylinePositions} />
      {init && <GestureHandlingSetter />}
    </ZoneContainer>
  );
};
